#----------------------------------------   Concatenating and Repeating Tuples   ---------------------------------#
#                                            ------------------------

"""
You can concatenate tuples (combine them) and repeat them (duplicate their contents).

Key Points:
1. Concatenate two tuples using the + operator.
2. Repeat a tuple by multiplying it with an integer using the * operator.
"""

# Example Tuples
tuple1 = (10, 20, 30)
tuple2 = (40, 50)

# 1. Concatenating Tuples
"""
Concatenate two tuples to form a new tuple.
"""
print("Tuple1:", tuple1)
print("Tuple2:", tuple2)
print("Concatenated tuple:", tuple1 + tuple2)

# 2. Repeating Tuples
"""
Repeat a tuple a certain number of times.
"""
print("Repeated tuple (tuple1 * 3):", tuple1 * 3)

