#----------------------------------------   Length and Size of a List   --------------------------------------------#
#                                               ----------------------------

"""
The length of a list refers to the number of elements in the list. Python provides built-in functions for finding the length.

Key Points:
1. Use `len()` to get the length of a list.
2. Lists can be empty, in which case the length is zero.
"""

#----------------------------------------------------------------------------------------------------------------------#

# Example List
my_list = [10, 20, 30, 40, 50]

# 1. Using len() to Find the Length of a List
"""
The len() function returns the number of elements in the list.
"""
list_length = len(my_list)
print("Length of the list:", list_length)

# 2. Empty List Example
"""
An empty list will have a length of zero.
"""
empty_list = []
print("Length of empty list:", len(empty_list))

