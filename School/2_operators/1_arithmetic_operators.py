#----------------------------------------   Arithmetic Operators in Python   ------------------------------------------#
#                                        -----------------------------------

"""
Arithmetic operators are used to perform mathematical operations like addition, subtraction, multiplication,
and division.
"""

# Key Arithmetic Operators:
'''
- Addition (+): Adds two operands.
- Subtraction (-): Subtracts the second operand from the first.
- Multiplication (*): Multiplies two operands.
- Division (/): Divides the first operand by the second operand (always results in a float).
- Floor Division (//): Divides the first operand by the second operand and returns the largest integer smaller than 
  or equal to the result.
- Modulus (%): Returns the remainder of dividing the first operand by the second.
- Exponentiation (**): Raises the first operand to the power of the second.
'''

# Examples:
a = 10
b = 3

print(f"Addition: {a} + {b} = {a + b}")
print(f"Subtraction: {a} - {b} = {a - b}")
print(f"Multiplication: {a} * {b} = {a * b}")
print(f"Division: {a} / {b} = {a / b}")
print(f"Floor Division: {a} // {b} = {a // b}")
print(f"Modulus: {a} % {b} = {a % b}")
print(f"Exponentiation: {a} ** {b} = {a ** b}")
